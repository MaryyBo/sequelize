SELECT * FROM tasks
WHERE user_id = 3
LIMIT 5
OFFSET 10;

-- 2 сторінка: скільки від першої сторінки треба пропустити : 5
-- 3 сторінка: 10
-- 4 сторінка: 15
-- 5 сторінка: 20


-- Як нам дізнатись, скільки потрібно відступати (формула для розрахунку OFFSET)
/*

У цій формулі, перша сторінка буде вважатись нульовою

OFFSET = LIMIT * сторінку_яку_ми_запитуємо - 1

*/

-- 2 сторінка: 5 * 1 = 5
-- 3 сторінка: 5 * 2 = 10
-- 4: 5 * 3 = 15
-- 5: 5 * 4 = 20